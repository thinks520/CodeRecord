#include <iostream>
using namespace std;

int main()
{
	int i = 1;//基本整型
	short j = 2;//短整型
	long k = 3;//长整型
	float ii = 1.1;//单精度型
	double jj = 1.234;//双精度型
	long double kk = 1.2342546;//扩展双精度型

	cout<<"i="<<i<<endl;
	cout<<"j="<<j<<endl;
	cout<<"k="<<k<<endl;
	cout<<"ii="<<ii<<endl;
	cout<<"jj="<<jj<<endl;
	cout<<"kk="<<kk<<endl;

	return 0;
} 